export function tasksReducer(tasks, action) {
  switch (action.type) {
    case "addTask":
      console.log("adding task")
      return {
        ...tasks,
        taskList: [...tasks.taskList, action.newTask],
      };
    case "deleteTask":
      console.log("deleting task")
      return {
        ...tasks,
        taskList: tasks.taskList.filter((t) => t.id !== action.id),
      };
    case "modifyTask":
      console.log("modifying tasks")
      return {
        ...tasks,
        taskList: tasks.taskList.map((t) =>
          t.id === action.id ? action.modifiedTask : t
        ),
      };
    case "updateTasks":
      console.log("updating tasks")
      return {
        ...tasks,
        taskList: tasks.taskList.map((t) =>
          t.category === action.previousCategory
            ? { ...t, category: action.updatedCategory }
            : t
        ),
      };
    case "setTasks":
      console.log("setting tasks")
      return {
        ...tasks,
        taskList: tasks.taskList.map((task) =>
          task.category === action.categoryTobeDeleted
            ? { ...task, category: null }
            : task
        ),
      };
  }
}
